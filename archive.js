function updateCaptions(index) {
  var captionFrame = document.getElementById("caption-frame");
  captionFrame.innerHTML = '';

  var imageData = risoImagePaths[index];
  if (imageData.text || imageData.doubleText) {
    if (window.innerWidth <= 850) {
      var captionText = imageData.text;
      var caption = document.createElement("div");
      caption.className = "caption";

      captionText.forEach(function (line) {
        var p = document.createElement("p");
        p.textContent = line;
        caption.appendChild(p);
      });

      captionFrame.appendChild(caption);
    } else if (window.innerWidth > 850) {
      var doubleText = imageData.doubleText;
      var doubleCaption = document.createElement("div");
      doubleCaption.className = "caption";

      doubleText.forEach(function (subArray) {
        var subCaption = document.createElement("div");
        subCaption.className = "caption";

        subArray.forEach(function (text) {
          var p = document.createElement("p");
          p.textContent = text;
          subCaption.appendChild(p);
        });
        captionFrame.appendChild(subCaption);
      });
    }
  }
}

