function adjustLayout() {
  var imgContainer = document.getElementById("feed");
/*   imgContainer.innerHTML = ''; */

  risoImagePaths.forEach(function (imageData, index) {
    var imgDiv = document.createElement("div");
    imgDiv.className = "article";
/*  imgDiv.id = imageData.id || ''; */

    var img = document.createElement("img");
    img.src = imageData.path;
    img.className = "works";
    img.id = imageData.id || '';

    if (imageData.basis === 1) {
      /* imgDiv.style.flexBasis = "100%"; */
      img.style.flexBasis = "100%";
    }

    imgContainer.appendChild(img)
/*     imgDiv.appendChild(img);
    imgContainer.appendChild(imgDiv); */

    img.addEventListener("load", function () {
      var observer = new IntersectionObserver(function (entries) {
        entries.forEach(function (entry) {
          if (entry.isIntersecting) {
            var index = risoImagePaths.indexOf(imageData);
            updateCaptions(index);
          }
        });
      }, { threshold: 0.5 });

      observer.observe(img);
    });
  });

  updateCaptions(0);
}

